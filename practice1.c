#include<stdio.h>

#define NUMBER 50 //配列aの要素数

int main(void)
{
	int num;
	int a[NUMBER]; //データ数の宣言

	printf("データ数：");
	do
	{
		scanf("%d", &num);
		if (num < 1 || num > NUMBER)
			printf("1〜%dで入力してください：", NUMBER);
	} while (num < 1 || num > NUMBER);

	int i;
	for (i = 0; i < num; i++)
	{
		printf("%2d番：", i + 1);
		scanf("%d", &a[i]);
	}

	printf("{");
	for (i = 0; i < num - 1; i++)
		printf("%d, ", a[i]);

	printf("%d}\n", a[num - 1]);

	return 0;

}

